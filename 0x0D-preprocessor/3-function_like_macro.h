#ifndef ABS_MACRO_H
#define ABS_MACRO_H

/*
 * File: 3-function_like__macro.h
 * Author: Mohammed Abba
 * Description: This header file defines a function-like macro named ABS(x)
 *		that calculates the absolute value of a number x.
 */

#define ABS(x) ((x) > 0 ? (x) : (-x))

#endif /* ABS_MACRO_H */
